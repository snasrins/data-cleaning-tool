# ─── Z-SCORE COMPUTATION ──────────────────────────────────────────────────────

def _get_lms(sex: str, indicator: str, age_months: int):
    """Return (L, M, S) for the given sex, indicator, and age in whole months.
    Supports ages 0-120 months (0-10 years) for WAZ, and extended ages for HAZ/BAZ.
    """
    sex_code = "M" if str(sex).strip().upper() in ("M", "MALE", "LELAKI", "1") else "F"
    table = _LMS_TABLES.get((sex_code, indicator))
    if table is None:
        return None
    age_int = int(round(age_months))
    age_int = max(0, min(120, age_int))  # Extended to 120 months (10 years)
    return table.get(age_int)


def compute_zscore(measurement: float, sex: str, age_months: float, indicator: str):
    """
    Compute WHO 2006 z-score.
    indicator: "WAZ" | "HAZ" | "BAZ"
    Returns: float z-score, or None if inputs are invalid.
    """
    try:
        if measurement is None or math.isnan(measurement): return None
        if age_months is None or math.isnan(age_months): return None
        lms = _get_lms(sex, indicator, age_months)
        if lms is None: return None
        L, M, S = lms
        if M <= 0 or S <= 0: return None
        if L == 0 or abs(L) < 1e-6:
            z = math.log(measurement / M) / S
        else:
            z = ((measurement / M) ** L - 1) / (L * S)
        # WHO recommends capping at ±6 for implausible values
        if abs(z) > 6:
            return None
        return round(z, 3)
    except Exception:
        return None


# ─── CLASSIFICATION ───────────────────────────────────────────────────────────

def classify_waz(z):
    """Weight-for-Age Z-score classification (WHO 2006)."""
    if z is None: return None
    if z < -3:   return "sangat_kurang_berat_badan"
    if z < -2:   return "kurang_berat_badan"
    if z <= 2:   return "berat_normal"
    return "berlebihan_berat_badan"

def classify_haz(z):
    """Height/Length-for-Age Z-score classification (WHO 2006)."""
    if z is None: return None
    if z < -3:   return "bantut_teruk"
    if z < -2:   return "bantut"
    if z <= 3:   return "tinggi_normal"
    return "tinggi"

def classify_baz(z):
    """BMI-for-Age Z-score classification (WHO 2006)."""
    if z is None: return None
    if z < -3:   return "susut_teruk"
    if z < -2:   return "susut"
    if z <= 1:   return "normal"
    if z <= 2:   return "risiko_berlebihan_berat_badan"
    if z <= 3:   return "berlebihan_berat_badan"
    return "obes"

